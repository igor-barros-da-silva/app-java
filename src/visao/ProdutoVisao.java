    private Boolean validaCampos() {
        String mensagem = "";
        Boolean retorno = true;
        if (cNome.getText().equals("")) {
            mensagem = mensagem + "O campo nome é obrigatório!\n";
            retorno = false;
        }
        if (cDescricao.getText().equals("")) {
            mensagem = mensagem + "O campo Descricao é obrigatório!\n";
            retorno = false;
        }
        if (retorno == false) {
            JOptionPane.showMessageDialog(null, mensagem);
        }
        return retorno;
    }
    
    public void montaTabela() {
        listaProdutos = HibernateUtil.getSession().
                createCriteria(Produto.class).list();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Nome");
        modelo.addColumn("Descrição");
        modelo.addColumn("ValorCusto");
        modelo.addColumn("ValorPedido");
        for (Produto p : listaProdutos) {
            modelo.addRow(new Object[]{p.getId(), p.getNome(), p.getDescricao(),
                p.getValorCusto(), p.getValorPedido()});
        }
        tabela.setModel(modelo);
    }
    
