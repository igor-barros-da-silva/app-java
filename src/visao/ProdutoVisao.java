    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (validaCampos()) {
            if (cCodigo.getText().equals("")) {
                prod.setId(null);
            } else {
                prod.setId(Long.parseLong(cCodigo.getText()));
            }

            prod.setNome(cNome.getText());
            prod.setDescricao(cDescricao.getText());
            prod.setValorCusto(new BigDecimal(cValorC.getText()));
            prod.setValorPedido(new BigDecimal(cValorV.getText()));

            HibernateUtil.beginTransaction();
            HibernateUtil.getSession().merge(prod);
            HibernateUtil.commitTransaction();
            HibernateUtil.closeSession();

            montaTabela();
            limpaCampos();
            validaTela("inicio");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btSalvarActionPerformed

